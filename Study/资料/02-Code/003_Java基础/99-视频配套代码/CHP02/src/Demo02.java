
public class Demo02 {

	public static void main(String[] args) {
		// Java数据类型
		
		//Java基本数据类型（8个基本类型）
		//(1)整型：byte short int long
		//(2)浮点：float double
		//(3)字符：char
		//(4)布尔：boolean
		
		//整型
		int i = 10;
		
		//浮点（默认情况下10.5是double类型，为了表示10.5是float类型，在数字后面加上字母f）
		float f = 10.5f;
		double d = 10.5;
		
		//字符(单引号，存储一个字符)
		char c = 'a';
		
		//布尔(值只能是true(真)或者false(假))
		boolean flag = true;
		
		//除了Java中的基本类型之外，其他数据类型，叫做引用。
		
		//第一个引用类型string(字符串)（可以存储多个字符）
		String str = "今天天气真好!";
		
		
		
		
		
		
		
		
		
		

	}

}
